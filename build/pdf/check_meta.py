"""Repo health check for document versions and dates.

1. Verifies that every rendered PDF in the outputs directory prints the date the
   registry (versions.json) says it should — catching any drift.
2. Regenerates VERSIONS.md (a human-readable mirror of the registry).

Run after re-rendering:  python build/pdf/check_meta.py
Exit code is non-zero if any PDF is missing or shows the wrong date.
"""
import json
import pathlib
import subprocess
import sys

ROOT = pathlib.Path("/home/claude/wetheusers")
OUT = pathlib.Path("/mnt/user-data/outputs")
DATA = json.loads((ROOT / "build/pdf/versions.json").read_text())

_MONTHS = ["January", "February", "March", "April", "May", "June",
           "July", "August", "September", "October", "November", "December"]


def fmt(iso):
    y, m, d = (int(x) for x in iso.split("-"))
    return f"{_MONTHS[m - 1]} {d}, {y}"


# Registry key -> rendered PDF filename in the outputs directory.
PDFS = {
    "founding-brief":            "We-the-Users-Founding-Brief.pdf",
    "declaration":               "We-the-Users-Declaration.pdf",
    "it-is-not-the-weather":     "We-the-Users-We-Never-Agreed.pdf",
    "shape-of-digital-shadow":   "We-the-Users-Your-Digital-Shadow.pdf",
    "ambition-against-ambition": "We-the-Users-Ambition-Against-Ambition.pdf",
    "the-longer-race":           "We-the-Users-The-Longer-Race.pdf",
    "the-second-zero":           "We-the-Users-The-Second-Zero.pdf",
    "the-ledger-and-the-spoils": "We-the-Users-The-Ledger-and-the-Spoils.pdf",
    "economic-foundations":      "We-the-Users-Economic-Foundations.pdf",
    "framer-invitation":         "We-the-Users-Framer-Invitation.pdf",
    "packet-start-here":         "We-the-Users-Start-Here.pdf",
    "cover-note":                "We-the-Users-Cover-Note.pdf",
}


def pdf_text(path):
    return subprocess.run(["pdftotext", "-layout", str(path), "-"],
                          capture_output=True, text=True).stdout


def validate():
    ok = True
    print(f"Checking dates against registry (as of {fmt(DATA['as_of'])}):\n")
    for key, fn in PDFS.items():
        p = OUT / fn
        want = fmt(DATA["documents"][key]["updated"])
        ver = DATA["documents"][key]["version"]
        if not p.exists():
            print(f"  MISSING   {fn}")
            ok = False
            continue
        if want in pdf_text(p):
            print(f"  ok        v{ver:<4} {want:<16} {fn}")
        else:
            print(f"  MISMATCH  expected {want!r} not found in {fn}")
            ok = False
    return ok


def regenerate_versions_md():
    d = DATA["documents"]
    rows = "\n".join(
        f"| {v['title']} | v{v['version']} | {fmt(v['updated'])} |" for v in d.values()
    )
    md = f"""# Document versions

_Generated by `build/pdf/check_meta.py` from `build/pdf/versions.json`, which is the
single source of truth for every document's version and date. Do not edit this file
by hand._

**How to update.** When a document changes substantively, edit its line in
`versions.json` — bump the version and set `updated` to today's `as_of` — then
re-render and run `python build/pdf/check_meta.py`. Each PDF's printed stamp is
generated from `versions.json`, so it cannot drift.

**Versioning.** `v0.x` = pre-launch drafts (bump the minor on each substantive
change). `v1.0` = the version published on **2026-07-02**. `v1.1`+ / `v2.0` =
post-launch amendments. A document's date is the day of its last substantive change;
typos and formatting-only edits do not bump the version.

_As of {fmt(DATA['as_of'])}._

| Document | Version | Last updated |
|---|---|---|
{rows}
"""
    (ROOT / "VERSIONS.md").write_text(md)
    print("\nVERSIONS.md regenerated.")


if __name__ == "__main__":
    ok = validate()
    regenerate_versions_md()
    print("\nALL GOOD." if ok else "\nPROBLEMS FOUND — see above.")
    sys.exit(0 if ok else 1)
